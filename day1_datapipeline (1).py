def min_mse_loss(y_true, y_pred):
    # y_true shape: (batch_size, 30*2) - flattened true trajectory
    # y_pred shape: (batch_size, 3*30*2) - flattened 3 predicted trajectories

    # Reshape y_true to (batch_size, 1, 30, 2) for easier broadcasting
    y_true_reshaped = tf.reshape(y_true, (-1, 1, 30, 2))

    # Reshape y_pred to (batch_size, num_modes, 30, 2)
    y_pred_reshaped = tf.reshape(y_pred, (-1, 3, 30, 2))

    # Calculate MSE for each mode
    # This will result in shape (batch_size, num_modes)
    mse_per_mode = tf.reduce_mean(tf.square(y_true_reshaped - y_pred_reshaped), axis=[2, 3])

    # Take the minimum MSE across all modes for each sample
    min_mse = tf.reduce_min(mse_per_mode, axis=1)

    # Return the mean of these minimum MSEs across the batch
    return tf.reduce_mean(min_mse)

