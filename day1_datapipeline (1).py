def inverse_transform_predictions(predictions_ac, X_original, mean, std):
    predictions_world = []
    for i in range(len(predictions_ac)):
        origin = X_original[i][-1] # Last point of the normalized past trajectory

        # Inverse the agent-centric shift
        # Each of the 3 predicted trajectories needs to be shifted by the origin
        shifted_predictions_ac = predictions_ac[i] + origin # shape (num_modes, 30, 2)

        # Inverse the normalization (using the global mean and std)
        denormalized_predictions = shifted_predictions_ac * std + mean
        predictions_world.append(denormalized_predictions)

    return np.array(predictions_world)


