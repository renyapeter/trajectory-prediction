class MultiModalLSTM(nn.Module):
    def __init__(self, input_dim=4, hidden=256, K=3, future_steps=6):
        super().__init__()
        self.K = K
        self.future_steps = future_steps

        # Encoder reads 4 past steps
        self.encoder = nn.LSTM(input_dim, hidden,
                               num_layers=2,
                               batch_first=True,
                               dropout=0.1)

        # K separate decoders — one per mode
        self.decoders = nn.ModuleList([
            nn.LSTM(2, hidden, num_layers=2, batch_first=True) # Added num_layers=2 here
            for _ in range(K)
        ])

        # K output heads
        self.output_heads = nn.ModuleList([
            nn.Linear(hidden, 2) for _ in range(K)
        ])

        # Confidence score per mode
        self.confidence = nn.Linear(hidden, K)

    def forward(self, x):
        # x: (B, 4, 4) — 4 timesteps, 4 features
        B = x.shape[0]

        # Encode past
        _, (h, c) = self.encoder(x)

        # Starting input = last observed position
        dec_input = x[:, -1:, :2]  # (B, 1, 2)

        all_modes = []
        for k in range(self.K):
            hk = h.clone()
            ck = c.clone()
            preds = []
            inp = dec_input.clone()

            for _ in range(self.future_steps):
                out, (hk, ck) = self.decoders[k](inp, (hk, ck))
                pred = self.output_heads[k](out)  # (B, 1, 2)
                preds.append(pred)
                inp = pred # Use predicted output as next input (autoregressive)

            mode = torch.cat(preds, dim=1)  # (B, 6, 2)
            all_modes.append(mode)

        # Stack all modes: (B, K, 6, 2)
        modes = torch.stack(all_modes, dim=1)

        # Confidence scores: (B, K)
        conf = F.softmax(
            self.confidence(h[-1]), dim=-1)

        return modes, conf


# WTA Loss — only backprop through best mode
def wta_loss(modes, conf, target):
    # modes:  (B, K, 6, 2)
    # target: (B, 6, 2)
    B, K, T, _ = modes.shape
    target_exp = target.unsqueeze(1).expand_as(modes) # (B, 1, 6, 2) -> (B, K, 6, 2)

    # ADE per mode: (B, K)
    ade_per_mode = torch.norm(
        modes - target_exp, dim=-1).mean(dim=-1) # L2 norm over (x,y) then mean over timesteps

    # Best mode index: (B,)
    best_idx = ade_per_mode.argmin(dim=1)

    # Best mode trajectory: (B, 6, 2) - select the trajectory from the best mode
    best_traj = modes[torch.arange(B), best_idx]

    # Regression loss on best mode only
    reg_loss  = F.mse_loss(best_traj, target)

    # Confidence loss — teach model which mode is best
    conf_loss = F.cross_entropy(conf, best_idx)

    return reg_loss + 0.3 * conf_loss


# Metrics
def compute_min_ade(modes, target):
    # modes:  (B, K, 6, 2)
    # target: (B, 6, 2)
    B, K, T, _ = modes.shape
    target_exp = target.unsqueeze(1).expand_as(modes)  # (B, 1, 6, 2) -> (B, K, 6, 2)
    ade_k = torch.norm(
        modes - target_exp, dim=-1).mean(dim=-1)  # (B, K) - ADE for each mode
    return ade_k.min(dim=1).values.mean().item() # Minimum ADE across modes for each sample, then mean over batch

def compute_min_fde(modes, target):
    B, K, T, _ = modes.shape
    pred_final = modes[:, :, -1, :]      # (B, K, 2) - last point of each predicted mode
    gt_final   = target[:, -1, :]        # (B, 2) - last point of ground truth
    fde_k = torch.norm(
        pred_final - gt_final.unsqueeze(1), dim=-1)  # (B, K) - FDE for each mode
    return fde_k.min(dim=1).values.mean().item() # Minimum FDE across modes for each sample, then mean over batch

