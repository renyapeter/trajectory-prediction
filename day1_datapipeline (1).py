def MinADE(y_true, y_pred_multi):
    # y_true: (samples, 30, 2)
    # y_pred_multi: (samples, num_modes, 30, 2)
    errors = []
    for i in range(len(y_true)):
        # Calculate ADE for each predicted mode against the single true trajectory
        ade_per_mode = np.mean(np.linalg.norm(y_true[i] - y_pred_multi[i], axis=2), axis=1) # (num_modes,)
        errors.append(np.min(ade_per_mode)) # Take the minimum ADE across modes for this sample
    return np.mean(errors)

def MinFDE(y_true, y_pred_multi):
    # y_true: (samples, 30, 2)
    # y_pred_multi: (samples, num_modes, 30, 2)
    errors = []
    for i in range(len(y_true)):
        # Calculate FDE for the last point of each predicted mode against the last true point
        fde_per_mode = np.linalg.norm(y_true[i,-1] - y_pred_multi[i,-1], axis=1) # (num_modes,)
        errors.append(np.min(fde_per_mode)) # Take the minimum FDE across modes for this sample
    return np.mean(errors)

