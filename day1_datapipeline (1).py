def rotate_to_heading(X, y):
    X_new, y_new = [], []

    for i in range(len(X)):
        dx = X[i][-1][0] - X[i][-2][0]
        dy = X[i][-1][1] - X[i][-2][1]

        theta = np.arctan2(dy, dx)

        R = np.array([
            [np.cos(-theta), -np.sin(-theta)],
            [np.sin(-theta),  np.cos(-theta)]
        ])

        X_rot = X[i] @ R.T
        y_rot = y[i] @ R.T

        X_new.append(X_rot)
        y_new.append(y_rot)

    return np.array(X_new), np.array(y_new)

