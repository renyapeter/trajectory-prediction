def check_predicted_trajectory_collision(pred_trajectories, collision_threshold=0.5):
    """
    Checks for 'collisions' (close proximity) between different predicted trajectories
    for a single sample at the same timesteps.

    Args:
        pred_trajectories (np.ndarray): Shape (num_modes, num_timesteps, 2)
                                        e.g., (3, 30, 2) for our case.
        collision_threshold (float): The maximum distance between two points
                                     at the same timestep to be considered a 'collision'.

    Returns:
        tuple: (bool, str) - True if any two predicted trajectories are 'colliding', False otherwise,
               and a message describing the collision status.
    """
    num_modes = pred_trajectories.shape[0]
    num_timesteps = pred_trajectories.shape[1]

    # Iterate through all unique pairs of predicted trajectories
    for i in range(num_modes):
        for j in range(i + 1, num_modes):
            # Compare trajectory i with trajectory j
            for t in range(num_timesteps):
                point1 = pred_trajectories[i, t]
                point2 = pred_trajectories[j, t]
                distance = np.linalg.norm(point1 - point2)
                if distance <= collision_threshold:
                    return True, f"Collision: Paths {i+1} & {j+1} at timestep {t+1}!"
    return False, "Collision: None detected."

