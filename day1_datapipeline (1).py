def min_mse_fde_loss(y_true, y_pred):
    y_true = tf.reshape(y_true, (-1, 1, 30, 2))
    y_pred = tf.reshape(y_pred, (-1, 3, 30, 2))

    mse = tf.reduce_mean(tf.square(y_true - y_pred), axis=[2,3])
    fde = tf.reduce_mean(tf.square(y_true[:,:,-1] - y_pred[:,:,-1]), axis=2)

    return tf.reduce_mean(tf.reduce_min(mse + 2*fde, axis=1))

